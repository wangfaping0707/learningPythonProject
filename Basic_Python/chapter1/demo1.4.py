import math
import cmath

# 导入一个模块中所有函数，可用以下语句
from math import *

print(pow(2, 5))
print(10 + pow(2, 3 * 5) / 3.0)
print(abs(-11))
print("-----------------------")
print(3 / 4)
# round将浮点数圆整为与之最接近的整数，并在两个整数一样近时圆整到偶数
print(round(1.4))
print(round(2.5))
print(round(2.6))
print(math.floor(32.9))
print(math.ceil(32.1))
print(cmath.sqrt(-1))
